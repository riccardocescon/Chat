     private boolean checkNumber(String text){
        for(int i = 0; i < text.length(); i++){
            if(text.charAt(i) < 48 || text.charAt(i) > 57){
                return false;
            }
        }
        return true;
    }
    
    public void run() {
        StringTokenizer st;
        String current;
        try {
            while(true){
                try {
                    mex = sIN.readLine();
                    if(mex.equals("END_CONNECTION_CONFIRM")){
                        chatPanel.setVisible(false);
                        Log.setVisible(true);
                        /*loginLx.setVisible(true);
                        loginRx.setVisible(true);*/
                        break;
                    }
                    switch(mex.substring(0, 3)){
                        case "/1:":
                            chatArea.append(mex.substring(3)+"\n");
                            st = new StringTokenizer(mex.substring(3), " ");
                            current = st.nextToken();
                            if(current.equals(name)) continue;
                            onlineList.append(current + "\n");
                        break;
                        
                        case "/2:":
                            onlineList.setText("");
                            st = new StringTokenizer(mex.substring(3), ";");
                            for(int i = 0; st.hasMoreTokens(); i++){
                                current = st.nextToken();
                                if(current.equals(name)) continue;
                                onlineList.append(current + "\n");
                            }
                        break;
                        
                        default:
                            chatArea.append(mex+"\n");
                            
                    }   
                    
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
            accept = 0;
            connection.close();
        } catch (IOException ex) {
            Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
