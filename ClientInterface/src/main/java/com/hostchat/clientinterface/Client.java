    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean clientData = false;
        boolean serverData = false;
        
        //CONTROLLO CREDENZIALI CLIENT
        if(!nameField.getText().equals("") && !passField.getText().equals("")){
            clientData = true;
            loginError.setVisible(false);
        }else{
            loginError.setVisible(true);
        }
        
        
        //CONTROLLO CREDENZIALI SERVER
        if(!serverField.getText().equals("")){
            serverData = true;
            loginError.setVisible(false);
        }else{
            loginError.setVisible(true);
        }
        
        //CONTROLLO PORTA
        /*if(checkNumber(portField.getText())){
            portNumber = true;
            errorLabel3.setVisible(false);
        }else{
            errorLabel3.setVisible(true);
        }*/
        
        if(clientData && serverData){
            name = nameField.getText();
            pass = passField.getText();
            
            try {
                connection = new Socket(server, port);

                out = connection.getOutputStream();
                sOut = new PrintWriter(out);
                in = new InputStreamReader(connection.getInputStream());
                sIN = new BufferedReader(in);

                sOut.println("CONFIRM");
                sOut.flush();
                sOut.println(0+";"+name+";"+pass);
                sOut.flush();	
                accept = Integer.parseInt(sIN.readLine());
            } catch (UnknownHostException e) {
                System.out.println("Connection Error");;
            } catch (IOException e) {
                e.printStackTrace();
            }
            if(accept==1){
                new Thread(this).start();
                Log.setVisible(false);
                /*loginLx.setVisible(false);
                loginRx.setVisible(false);*/
                chatPanel.setVisible(true);
                
            }else if (accept==0){
                loginError.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

